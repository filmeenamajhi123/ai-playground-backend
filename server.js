// =========================
// FEATURE 4: GEMINI CHAT (Ask Gemini)
// =========================
app.post("/ask", async (req, res) => {
  try {
    const { question } = req.body;

    if (!question) {
      return res.status(400).json({ answer: "Question is required" });
    }

    const answer = await callGemini(question);
    res.json({ answer });

  } catch (error) {
    console.error("Server error in /ask:", error);
    res.status(500).json({ answer: `Error: ${error.message}` });
  }
});

// =========================
// SERVER START
// =========================
app.listen(5000, () => {
  console.log("Merged Server running at http://localhost:5000");
  console.log("Features: Fun Fact, Journal Prompt, Helper Bot, Gemini Chat");
});
